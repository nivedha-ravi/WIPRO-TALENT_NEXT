package wrapper_classes;

public class Binary_Octal_hexadecimal {

	public static void main(String[] args) {
		Integer x=20;
		System.out.println("Binary Representation: "+Integer.toBinaryString(x));
		System.out.println("Octal Representation: "+Integer.toOctalString(x));
		System.out.println("HexaDecimal Representation: "+Integer.toHexString(x));
		

	}

}
